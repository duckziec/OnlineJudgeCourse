    public void loadCourseData(Course course) {
        if (course == null) {
            return;
        }

        this.currentCourse = course;

        // Cập nhật tiêu đề
        jLabel1.setText(course.getTitle());

        // Cập nhật giá
        lbGia.setText(course.getPrice() == 0 ? "Free" : course.getPrice() + " VND");

        // Cập nhật mô tả
        txtDescription.setText(course.getDescription());

        // Load hình ảnh
        loadCourseImage(course.getCourseId());

        // Load các section (phần học)
        loadCourseSections(course);
    }

    private void loadCourseImage(int courseId) {
        String imagePath = CourseData.getImagePath(courseId);
        imagePath = imagePath.replace("/images610x360/", "/images/");
        try {
            ImageIcon icon = new ImageIcon(getClass().getResource(imagePath));
            if (icon.getIconWidth() > 0) {
                txtPicture.setIcon(icon);
            }
        } catch (Exception e) {
            System.err.println("Không thể load hình: " + imagePath);
        }
    }

    private void loadCourseSections(Course course) {
        // Xóa các section cũ
        mainPanel.removeAll();

        // Tạo sections mẫu dựa vào content
        List<String> sections = parseSectionsFromContent(course.getContent());
        //CustomDialog.getInstance().showSuccess(course.getContent(), "10239");

        for (int i = 0; i < sections.size(); i++) {
            String sectionTitle = "Phần " + (i + 1) + ": " + sections.get(i);
            Section section = new Section(
                    sectionTitle,
                    "3 Lessons",
                    "30 Mins",
                    List.of("Bài học 1", "Bài học 2", "Bài học 3")
            );
            mainPanel.add(section);
        }

        mainPanel.revalidate();
        mainPanel.repaint();
    }

