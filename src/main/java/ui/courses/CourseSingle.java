    private void btStartNowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btStartNowActionPerformed
        // TODO add your handling code here:
        User currenrUser = SessionManager.getInstance().getCurrentUser();
        if (currenrUser == null) {
            CustomDialog.getInstance().showError("Vui lòng đăng nhập để tiếp tục.", "Thông báo");
        } else {
            if (currentCourse.getPrice() == 0) {
                try {
                    boolean success = AppConfig.getEnrollmentService().enrollCourse(currenrUser.getUserId(), currentCourse.getCourseId());
                    if (success) {
                        CustomDialog.getInstance().showSuccess("Bạn đã đăng ký khóa học thành công!", "Thông báo");
                        CourseData.reloadUserCourses();
                    } else {
                        CustomDialog.getInstance().showError("Đăng ký khoá học thất bại!\nVui lòng liên hệ admin để được hỗ trợ.", "Error Message");
                    }
                } catch (ExistException e) {
                    CustomDialog.getInstance().showError(e.getMessage(), "Error Message");
                }
            } else {
                CustomDialog.getInstance().showError("Khóa học này có phí.\nChức năng thanh toán sẽ được cập nhật trong thời gian tới.", "Thông báo");
            }
        }


    }//GEN-LAST:event_btStartNowActionPerformed

