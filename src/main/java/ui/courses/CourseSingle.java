    private List<String> parseSectionsFromContent(String content) {
        List<String> sections = new ArrayList<>();
        if (content == null) {
            return sections;
        }

        String[] lines = content.split("\n");
        for (String line : lines) {
            line = line.trim();
            if (line.matches("^\\d+\\..*")) {
                sections.add(line.substring(line.indexOf(".") + 1).trim());
            }
        }

        return sections;
    }

