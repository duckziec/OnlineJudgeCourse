    private void selectRememberMe(String usernameOrEmail) {
        // === START: Remember Me Logic ===
        java.util.prefs.Preferences prefs = java.util.prefs.Preferences.userNodeForPackage(Login.class);
        if (jCheckBox1.isSelected()) {
            // Save the username and the preference
            prefs.put("username", usernameOrEmail);
            prefs.putBoolean("rememberMe", true);
        } else {
            // Clear the preferences if the box is not checked
            prefs.remove("username");
            prefs.remove("rememberMe");
        }
        // === END: Remember Me Logic ===
    }


