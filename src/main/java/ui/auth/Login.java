    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed

        String usernameOrEmail = txtTK.getText().trim();
        String password = new String(txtKey.getPassword());

        if (usernameOrEmail.isEmpty() || password.isEmpty()) {
            CustomDialog.getInstance().showWarning("Vui lòng nhập đầy đủ thông tin!", "Thông báo");
            return;
        }

        try {
            User user = AppConfig.getUserService().login(usernameOrEmail, password);
            //CustomDialog.getInstance().showSuccess(user.toString());
            if (user != null) {
                SessionManager.getInstance().login(user);
                CustomDialog.getInstance().showSuccess("Welcome " + user.getFullName(), "Login Success");
                // handleSuccessfulLogin(user);
                if (parentFrame != null) {
                    parentFrame.updateLoginStatus();
                    // Chuyển về trang Home
                    parentFrame.navigateToHome();
                }

                selectRememberMe(usernameOrEmail);

                // Xóa dữ liệu nhập
                clearFields();
            } else {
                CustomDialog.getInstance().showError("Tên đăng nhập hoặc mật khẩu không đúng!", "Thông báo");
            }
        } catch (ValidationException e) {
            CustomDialog.getInstance().showError("Login failed: " + e.getMessage(), "Error Message");
        } catch (Exception e) {
            CustomDialog.getInstance().showError("An unexpected error occurred: " + e.getMessage(), "Error Message");
            System.err.println(e.getMessage());
        }


    }//GEN-LAST:event_btLoginActionPerformed

