    private void btXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXacNhanActionPerformed
        String email = txtEmail.getText().trim();
        String code = txtOTP.getText().trim();
        String password = txtPass1.getText().trim();
        String confirmPassword = txtCNPass.getText().trim();
        if (password.equals(confirmPassword)) {
            try {
                boolean success = AppConfig.getUserService().resetPasswordWithCode(email, code, password);
                if (success) {
                    CustomDialog.getInstance().showSuccess("Đã đổi mật khẩu mới! Vui lòng đăng nhập lại", "Thông báo");
                    if (parentFrame != null) {
                        Login loginPanel = new Login();
                        loginPanel.setParentFrame(parentFrame);
                        parentFrame.getBodyScrollPane().setViewportView(parentFrame.wrapPanelCenter(loginPanel));
                        parentFrame.getBodyScrollPane().revalidate();
                        parentFrame.getBodyScrollPane().repaint();
                    }
                }
            } catch (ValidationException e) {
                CustomDialog.getInstance().showError(e.getMessage(), "Error Message");
            } catch (Exception e) {
                CustomDialog.getInstance().showError("Lỗi trong việc đặt lại mật khẩu: " + e.getMessage(), "Error Message");
            }
        } else {
            CustomDialog.getInstance().showError("Mật khẩu không khớp", "Thông báo");
        }
    }//GEN-LAST:event_btXacNhanActionPerformed

