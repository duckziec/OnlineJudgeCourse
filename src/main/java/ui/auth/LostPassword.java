    private void btGuiOTPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuiOTPActionPerformed
        String email = txtEmail.getText().trim();
        try {
            Validator.validateEmail(email);
            CustomDialog.getInstance().showSuccess("Đang gửi mã OTP...", "Thông báo");
            boolean success = AppConfig.getUserService().sendPasswordResetCode(email);
            if (success) {
                CustomDialog.getInstance().showSuccess("Đã gửi mã OTP", "Thông báo");
                btGuiOTP.setEnabled(false);
                CustomKey.getInstance().bindEnterKeyToContainer(this, btXacNhan);
            }
        } catch (ValidationException e) {
            CustomDialog.getInstance().showError(e.getMessage(), "Thông báo");
        } catch (Exception e) {
            CustomDialog.getInstance().showError("Lỗi trong việc gửi OTP: " + e.getMessage(), "Thông báo");
        }
    }//GEN-LAST:event_btGuiOTPActionPerformed

